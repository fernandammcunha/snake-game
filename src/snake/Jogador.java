package snake;

public class Jogador {

    public Jogador() {
        this.nome = nome;
        this.score = score;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    private String nome;
    private int score;

}
